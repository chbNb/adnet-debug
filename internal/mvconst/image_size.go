package mvconst

const (
	IMAGE_SIZE_ID_UNKOWN = 0
	IMAGE_SIZE_ID_320X50 = iota + 1
	IMAGE_SIZE_ID_300X250
	IMAGE_SIZE_ID_480X320
	IMAGE_SIZE_ID_320X480
	IMAGE_SIZE_ID_300X300
	IMAGE_SIZE_ID_1200X627
	REWARD_VIDEO_SIZE_ID
	IMAGE_SIZE_ID_1080x540 = iota + 9
	IMAGE_SIZE_ID_720x1280
	IMAGE_SIZE_ID_640x640
	IMAGE_SIZE_ID_720x576
)

const (
	IMAGE_SIZE_STR_320X50   = "320x50"
	IMAGE_SIZE_STR_300X250  = "300x250"
	IMAGE_SIZE_STR_480X320  = "480x320"
	IMAGE_SIZE_STR_320X480  = "320x480"
	IMAGE_SIZE_STR_300X300  = "300x300"
	IMAGE_SIZE_STR_1200X627 = "1200x627"
	REWARD_VIDEO_SIZE_STR   = "VIDEO"
	IMAGE_SIZE_STR_128x128  = "128x128"
	IMAGE_SIZE_STR_1080x540 = "1080x540"
	IMAGE_SIZE_STR_720x1280 = "720x1280"
	IMAGE_SIZE_STR_640x640  = "640x640"
	IMAGE_SIZE_STR_720x576  = "720x576"
)

var ImageSizeMap = map[int]string{
	IMAGE_SIZE_ID_320X50:   IMAGE_SIZE_STR_320X50,
	IMAGE_SIZE_ID_300X250:  IMAGE_SIZE_STR_300X250,
	IMAGE_SIZE_ID_480X320:  IMAGE_SIZE_STR_480X320,
	IMAGE_SIZE_ID_320X480:  IMAGE_SIZE_STR_320X480,
	IMAGE_SIZE_ID_300X300:  IMAGE_SIZE_STR_300X300,
	IMAGE_SIZE_ID_1200X627: IMAGE_SIZE_STR_1200X627,
	REWARD_VIDEO_SIZE_ID:   REWARD_VIDEO_SIZE_STR,
	IMAGE_SIZE_ID_1080x540: IMAGE_SIZE_STR_1080x540,
	IMAGE_SIZE_ID_720x1280: IMAGE_SIZE_STR_720x1280,
	IMAGE_SIZE_ID_640x640:  IMAGE_SIZE_STR_640x640,
	IMAGE_SIZE_ID_720x576:  IMAGE_SIZE_STR_720x576,
}

func GetImageSizeID(imageSize string) int {
	for k, v := range ImageSizeMap {
		if v == imageSize {
			return k
		}
	}
	return IMAGE_SIZE_ID_UNKOWN
}

func GetImageSizeByID(id int) string {
	if len(ImageSizeMap[id]) <= 0 {
		return IMAGE_SIZE_STR_128x128
	}
	return ImageSizeMap[id]
}
